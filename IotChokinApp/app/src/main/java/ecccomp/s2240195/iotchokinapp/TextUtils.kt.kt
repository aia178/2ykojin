    private val KEYWORDS_REGEX: Regex by lazy {
        REMOVE_KEYWORDS.joinToString("|") { Regex.escape(it) }.toRegex()
    }

    // 可変パターン
    private val REGEX_REMOVALS: List<Regex> = listOf(
        Regex("ポイント\\s*(最大)?\\s*\\d+\\s*倍"),
        Regex("\\d+\\s*(％|%)\\s*OFF", RegexOption.IGNORE_CASE),
